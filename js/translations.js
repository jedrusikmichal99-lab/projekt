let currentLang = localStorage.getItem('preferredLanguage') || 'pl';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('preferredLanguage', lang);
    document.documentElement.lang = lang;

    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = getNestedTranslation(translations[lang], key);
        if (translation) {
            if (element.tagName === 'INPUT' && element.type !== 'submit') {
                element.placeholder = translation;
            } else if (element.tagName === 'OPTION') {
                element.textContent = translation;
            } else {
                element.textContent = translation;
            }
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        const translation = getNestedTranslation(translations[lang], key);
        if (translation) {
            element.placeholder = translation;
        }
    });

    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });

    updateMetaTags(lang);
}

function getNestedTranslation(obj, path) {
    return path.split('.').reduce((current, key) => {
        return current && current[key] !== undefined ? current[key] : null;
    }, obj);
}

function updateMetaTags(lang) {
    const t = translations[lang];

    const titleEl = document.querySelector('title');
    if (titleEl && t.hero) {
        const titles = {
            pl: 'Biura Ogrodowe | Wysoka Jakosc i 15 Lat Doswiadczenia',
            en: 'Garden Offices | High Quality and 15 Years of Experience',
            de: 'Gartenburos | Hohe Qualitat und 15 Jahre Erfahrung'
        };
        titleEl.textContent = titles[lang] || titles.pl;
    }

    const descMeta = document.querySelector('meta[name="description"]');
    if (descMeta && t.hero) {
        descMeta.content = t.hero.subtitle;
    }
}

document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLang);
});
